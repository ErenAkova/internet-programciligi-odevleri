<font color="phosphorescent green">Marvel’ın Bilinmeyenleri Listesi
Sizler için Marvel’ın Bilinmeyenleri yazımızda birçok bilgi vererek hayranı olduğumuz bu evrenin ayrıntılarına inecek, detaylarıyla analiz edeceğiz. Böylelikle, takip ettiğiniz filmleri hakkında sizler de daha çok bilgi öğrenmiş olacaksanız. Lafı fazla uzatmadan listemize geçelim.
<br />
<font color="red">Wakanda Kurgu Değil
Wakanda’nın resmi dili aslında gerçek bir dil. Zimbabve’de ve Güney Afrika’da konuşulan Xhosa lehçesinden esinleniyor. Bu iki lokasyon ilgili evrendeki Wakanda ülkesine de çok yakınlar.
</font><br />
<font color="black">DC ve Marvel
Disney’in şu anda sahip olduğu firma, zamanında DC’yi satın alabilirdi. Marvel’ın 1984 yılındaki yükselişinden sonra şirket editörü Jim Shooter, DC’ye haklarını satın almak için bir teklif sundu fakat teklif DC tarafından reddedildi. Daha sonra 1996 yılında şirket iflas ettiğinde DC alanında bir numaraydı.
</font><br />
<font color="black and purple">S.H.I.E.L.D’in Kökeni
SHIELD, faaliyetlerine Brotherhood of SHIELD olarak Antik Mısır zamanında başlamıştı. Leonardo DaVinci ve Isaac Neywon gibi kişilikler de bu oluşumda yer almıştır. 2010 yılında yayınlanan SHIELD çizgi romanında bu konudan bahsediliyor.
</font><br />
<font color="purple">Marvel'ın Bilinmeyenleri
Jihad
Jihad, dünyayı tamamen ele geçirmek ve yıkmak amacı güden bir karakterdir. Fakat 9-11 saldırısından sonra duyarlılık açısından karakter iptal edilmiştir.
</font><br />
<font color="orange">Thor’un İkizi
Evrende Thor ile tıpatıp benzeyen bir karakter var. Duyduğunuza çok şaşıracaksınız fakjat bu karakter bir kurbağa. Evet yanlış anlamadınız bir kurbağa üstelik ismi de Throg. Sihirli çekicinin ismi ise Frog Mjolnir.
</font><br />
<font color="green and black">Marvel’ın Bilinmeyenleri Listesi: Michael Jackson ve Hayali
Dünyaca ünlü pop yıldızı Jackson 90’lı yılların başında firmayı satın almayı denedi. Peki amacı neydi. Jackson, Spider-Man karakterinin film versiyonunda başrol olmak istiyordu.
</font><br />
<font color="blue and green">Marvel'ın Bilinmeyenleri
Venom Nasıl Yaratıldı?
Firma fanları için düzenlediği bir yarışma esnasında Randy Schueller isimli bir fan tarafından gelen öneri karşısında şok oldu. Schueller Spider-Man’ın kostümünün siyah olması ve gizli işlere karışmasını öneren bir tasarı sundu. Firma editörleri bu fikirden yola çıkarak Venom’u yarattılar ve Randy Schueller’e 220 dolarlık bir ödül verdiler.
</font><br />
<font color="red and purple">Kurtadamlar ve Vampirler
Comics Code Authority bir zamanlar karikatürlerde vampirlerin ve kurtadamların kullanılmasını yasaklamıştı. Fakat firma, kurtadamların klasik bir kötü adam tipi olduğunu ve kullanılmamasını çok zor olduğunu savunarak buna karşı çıktı. 60’lı yıllarda X-Men’deki Beast karakterini yayınlamalrı ise bu karşı çıkışa bir örnektir.
</font><br />
<font color="green and purple">Siyah Giyen Adamlar
Siyah Giyen Adamlar asıl olarak Malibu Comics’e ait olmasına rağmen firma bütün hakları 1994 yılında almıştı. Bu demek oluyor ki Siyah Giyen Adamlar hakkında yapılan bütün filmnler,oyunlar ve diziler firma tarafından üretildi.
</font><br />
<font color="black">Marvel'ın Bilinmeyenleri
En Zengin Thor
Thor’un bir Tanrı karakter olduğunu unutmamak gerekli. Ailesi çeşitli dünyalardan toplanan hazine ve değerli eşyalarla dolu bir ganimete sahip. Bu da Thor’u film ve çizgi roman evreninin en zengin karakteri yapıyor.
</font><br />
<font color="bold black">Marvel’ın İrlandalıları
Marvel’da ünlü süper kahramanların çoğu İrlanda kökenli. Bunlardan bazıları Captain America, Beast,Daredevil ,Bullseye ve Ant-Man
</font><br />
